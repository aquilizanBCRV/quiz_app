    private void nextPageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextPageActionPerformed
        // TODO add your handling code here:
        sendAnswer(); // save current answer FIRST

        if (currentIndex < quizQuestions.size() - 1) {
            currentIndex++;
            labelDisplayQuiz.setText(quizQuestions.get(currentIndex));
            restartTimer();
            displayAnswerList(currentIndex);
            getCounter();
        } else {
            labelDisplayQuiz.setText("End of quiz");
            timer.stop();
            labelTimer.setText("00:00");
        }
    }//GEN-LAST:event_nextPageActionPerformed
    private void displayAnswerList(int currentIndex) {
        String[] output = listQuestion.get(currentIndex).split(",");

        JPanel[] boxes = {box1, box2, box4, box6};
        JRadioButton[] questions = {selection_1, selection_2, selection_4, selection_6};

        for (int i = 0; i < boxes.length; i++) {
            if (i < output.length && !output[i].trim().isEmpty()) {
                boxes[i].setVisible(true);
                questions[i].setActionCommand(String.valueOf(i));
                questions[i].setText(output[i].trim());
            } else {
                boxes[i].setVisible(false);
            }
        }

        // refresh layout after visibility changes
        box1.getParent().revalidate();
        box1.getParent().repaint();
    }

