    private void initializeQuiz() {
        Quiz quiz = new Quiz(conn);
        quiz.setQuizGroupID(1);
        ResultSet quizList = quiz.displayQuiz();

        try {
            while (quizList.next()) {
                // Assuming column name is "question"
                listQuestion.add(quizList.getString("questionList"));
                quizQuestions.add(quizList.getString("displayQuestion"));

                ++totalQuizes;
            }

            // Show first quiz
            if (!quizQuestions.isEmpty()) {
                labelDisplayQuiz.setText(quizQuestions.get(currentIndex));
                displayAnswerList(currentIndex);
                getCounter();
            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }

    private void customInit() {
        buttonGroup1.add(selection_1);
        buttonGroup1.add(selection_2);
        buttonGroup1.add(selection_4);
        buttonGroup1.add(selection_6);

        // Stop existing timer if running
        if (timer != null && timer.isRunning()) {
            timer.stop();
        }

        // Reset counter
        counter = 0;

        // Create new timer
        timer = new Timer(1000, new ActionListener() { // 1000ms = 1 second
            @Override
            public void actionPerformed(ActionEvent e) {
                counter++;

                int minutes = counter / 60;
                int seconds = counter % 60;

                labelTimer.setText(String.format("%02d:%02d", minutes, seconds));
            }
        });

        timer.start();
    }

    private void restartTimer() {
        if (timer != null) {
            timer.stop();
        }

        counter = 0;
        labelTimer.setText("00:00");
        timer.start();
    }

    private void getCounter() {
        counterPage.setText("Page %d to %d".formatted(
                currentIndex + 1,
                totalQuizes
        ));
    }

    private void sendAnswer() {
        ButtonModel selectedModel = buttonGroup1.getSelection();

        log.setQuizID(1);
        log.setStudentID(1);
        log.setStudentAnswer(
                buttonGroup1.getSelection().getActionCommand());
        log.setTimestamp(LocalTime.of(0, 0, counter));
        log.createQuiz();
    }
